public class MyDestination {
    String DestName;
    double Lat;
    double Long;


    public double getLat() {
        return Lat;
    }

    public void setLat(double lat) {
        Lat = lat;
    }

    public String getDestName() {
        return DestName;
    }

    public void setDestName(String destName) {
        DestName = destName;
    }

    public double getLong() {
        return Long;
    }

    public void setLong(double aLong) {
        Long = aLong;
    }




}
